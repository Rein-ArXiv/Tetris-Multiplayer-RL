// 한 글리프 폭 = 5 픽셀 + 1 픽셀 spacing = 6 픽셀 원본 단위.
static constexpr int GLYPH_W = 5;
static constexpr int GLYPH_H = 7;
static constexpr int SPACING = 1;

static int glyph_row_px(int size) {
    // size = 픽셀 단위 폰트 높이. 한 셀 크기 = size / GLYPH_H (>=1).
    int s = size / GLYPH_H;
    return s < 1 ? 1 : s;
}

int measure_text(const char* text, int size)
{
    if (!text || !text[0]) return 0;
    int cell = glyph_row_px(size);
    int chars = (int)strlen(text);
    return chars * (GLYPH_W + SPACING) * cell;
}

void draw_text(const char* text, int x, int y, int size, Color c)
{
    if (!text || !text[0]) return;
    int cell = glyph_row_px(size);

    // 배치: 각 "on" 픽셀마다 6개 꼭짓점(두 삼각형 = 한 사각형).
    // 대략 한 글리프당 최대 35픽셀, 평균 15픽셀 정도.
    std::vector<float> verts;
    verts.reserve(strlen(text) * 15 * 12);

    auto emit = [&](float fx, float fy, float fw, float fh) {
        verts.push_back(fx);       verts.push_back(fy);
        verts.push_back(fx);       verts.push_back(fy + fh);
        verts.push_back(fx + fw);  verts.push_back(fy + fh);
        verts.push_back(fx);       verts.push_back(fy);
        verts.push_back(fx + fw);  verts.push_back(fy + fh);
        verts.push_back(fx + fw);  verts.push_back(fy);
    };

    int penX = x;
    for (const char* p = text; *p; ++p) {
        unsigned ch = (unsigned char)*p;
        if (ch < 32 || ch > 126) {
            penX += (GLYPH_W + SPACING) * cell;
            continue;
        }
        const uint8_t* g = kFont5x7[ch - 32];
        for (int col = 0; col < GLYPH_W; ++col) {
            uint8_t bits = g[col];
            for (int row = 0; row < GLYPH_H; ++row) {
                if (bits & (1 << row)) {
                    float px = (float)(penX + col * cell);
                    float py = (float)(y    + row * cell);
                    emit(px, py, (float)cell, (float)cell);
                }
            }
        }
        penX += (GLYPH_W + SPACING) * cell;
    }

    if (verts.empty()) return;

    GLuint prog = renderer_get_rect_prog();
    GLuint vao  = renderer_get_rect_vao();
    GLuint vbo  = renderer_get_rect_vbo();
    glUseProgram(prog);
    glUniformMatrix4fv(renderer_get_rect_proj(), 1, GL_FALSE, renderer_get_proj());
    glUniform4f(renderer_get_rect_color(),
        c.r / 255.0f, c.g / 255.0f, c.b / 255.0f, c.a / 255.0f);

    glBindVertexArray(vao);
    glBindBuffer(GL_ARRAY_BUFFER, vbo);
    glBufferData(GL_ARRAY_BUFFER, (GLsizeiptr)(verts.size() * sizeof(float)),
                 verts.data(), GL_DYNAMIC_DRAW);
    glDrawArrays(GL_TRIANGLES, 0, (GLsizei)(verts.size() / 2));
    glBindVertexArray(0);
}
