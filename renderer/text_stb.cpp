// renderer/text_stb.cpp — 크로스플랫폼 텍스트 백엔드
//
// SDL2 빌드(Mac/Linux)에서 사용. 순수 OpenGL 코어 프로파일이라
// wglUseFontBitmaps / glCallLists 같은 compat-profile API를 쓸 수 없다.
//
// 구현 전략:
//   - 5x7 비트맵 폰트(ASCII 32–126)를 컴파일타임 상수 테이블로 임베드.
//   - draw_text 는 각 "on" 픽셀을 작은 정사각형으로 모아 단일 draw call 로
//     rect 셰이더에 업로드(batched quads).
//   - 퍼포먼스: 한 줄 텍스트 ≈ 100~300개 quad — 게임 UI 수준엔 충분.
//
// 제약: TTF 파일을 사용하지 않는다(monogram.ttf 무시). 폰트 크기에 따라
//       픽셀 배율만 바뀌며, 높은 사이즈에서는 stairstep이 보일 수 있다.
//       향후 stb_truetype 도입 시 이 파일만 교체.

